// BIBLIOTECA <cmath>
/* Comandos úteis
pow(base, expoente): Calcula a potência. Ex: pow(2, 3) resulta em 8.0.

sqrt(x): Calcula a raiz quadrada.Ex: sqrt(9) resulta em 3.0.

cbrt(x): Calcula a raiz cúbica.

ceil(x): Arredonda para cima (teto). Ex: ceil(4.1) vira 5.0.

floor(x): Arredonda para baixo (piso). Ex: floor(4.9) vira 4.0.

round(x): Arredonda para o mais próximo. Ex: round(4.4) vira 4.0, mas round(4.5) vira 5.0.

sin(x) / cos(x) / tan(x): Calculam o seno, cosseno e tangente. Atenção: o ângulo x deve estar obrigatoriamente em radianos, e não em graus.

hypot(x, y): Calcula a hipotenusa de um triângulo retângulo dados os catetos raizquadrada(x^2 + y^2). É perfeita para calcular a distância euclidiana entre dois pontos no plano cartesiano.

abs(x) ou fabs(x): Retorna o valor absoluto (módulo), ou seja, transforma números negativos em positivos. Ex: abs(-5) vira 5.

*/